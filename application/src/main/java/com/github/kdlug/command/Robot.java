package com.github.kdlug.command;

public class Robot {
    void turnOn() {
        System.out.println("Robot enabled");
    }

    void cut() {
        System.out.println("Robot is cutting");
    }

    void drill() {
        System.out.println("Robot is drilling");
    }

    void turnOff() {
        System.out.println("Robot disabled");
    }
}
